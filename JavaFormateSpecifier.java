public class JavaFormateSpecifier {
    public static void main(String[]args){
        int a = 1049;
        float f = 1049.32f;//in java float value with add (last  f )must.
        double d = 221.35;

        System.out.printf("Integer %d\n",a);
        System.out.printf("float %.2f\n",f);
        System.out.printf("double %.3f\n",d);
/*
We can fix print value with (%.2f)
 */
    }
}
