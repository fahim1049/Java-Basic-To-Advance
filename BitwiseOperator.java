public class BitwiseOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 30;

        int c;

        c = a & b;
        System.out.println("a & b = " +c);

        c = a|b;

        System.out.println("a | b = " +c);

        c = a^b;

        System.out.println("a^b = " +c);
        

    }
}
