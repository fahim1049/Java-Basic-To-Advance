public class EscapeSequence {
    public static void main(String[] args){
        System.out.println("My name Is \t Fahim"); //print with tab
        System.out.println("Name \be Fahim");//backspace remove
        System.out.println("Fahim is \ngood boy");
        System.out.println("Fahim is a good boy \r He is 24 years old");//return
        System.out.println("Fahim ID  \'221-35-1049\'");//single quote
        System.out.println("\"Fahim\"");//double quote
    }
}
